###### 安装X-ui的函数 ######
function install_Xui {
   if which "x-ui" >/dev/null 2>&1; then
      echo "Xui面板已经安装！"
   else
      bash <(curl -Ls https://raw.githubusercontent.com/vaxilu/x-ui/master/install.sh)
   fi
}


